            consultas.updateMateria(cmb_grupo.getSelectedItem().toString(), cmb_materia.getSelectedItem().toString(), txtAnio.getText(), cmbx_periodo.getSelectedItem().toString(), cmb_unidad1.getSelectedItem().toString(), cmb_hora.getSelectedItem().toString(),comboMaestros1.getSelectedItem().toString());
