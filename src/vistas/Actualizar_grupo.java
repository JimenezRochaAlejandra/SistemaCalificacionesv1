    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Calendar calendario = Calendar.getInstance();
        Integer year = calendario.get(Calendar.YEAR);
        txtAnio.setText(String.valueOf(year));
        
        setLocationRelativeTo(null);
        txtAnio.setText(String.valueOf(year)); //Se cambia el año por el del sistema actual
        nombreUsuarios = consultas.getCurp();
        //Se rellena el combo box
        for (int i = 0; i < nombreUsuarios.length; i++) {
            comboMaestros1.addItem(nombreUsuarios[i]);
        }
    }//GEN-LAST:event_formWindowOpened

    private void cmb_materiaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_materiaItemStateChanged
        if (evt.getItem() == "Agregar otra...") {
            this.dispose();
            AgregarMateria agregarMateria = new AgregarMateria();
            agregarMateria.setVisible(true);
        }
    }//GEN-LAST:event_cmb_materiaItemStateChanged

